    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed

        int a = JOptionPane.showConfirmDialog(parent, "Delete product?");
        //0 yes
        //1 no
        //2 cancel
        //-1 exit
        if (a==0) {
            Long id = Long.parseLong(txtId.getText().trim());
            Controller.getInstance().removeProduct(id);
            JOptionPane.showMessageDialog(parent, "Product is deleted");
            prepareForm(ModeForm.VIEW);
            jbtnEnableChanges.setVisible(false);
            //fps.refresTable();
        } 
    }//GEN-LAST:event_jbtnDeleteActionPerformed


