        Manufacturer manufacturer = (Manufacturer) comboManufacturer.getSelectedItem();
        MeasureUnit measureUnit = (MeasureUnit) comboMeasureUnit.getSelectedItem();
        BigDecimal price = new BigDecimal(txtPrice.getText().trim());

        Product product = Controller.getInstance().searchProduct(id);
        product.setName(name);
        product.setDescription(desc);
        product.setManufacturer(manufacturer);
        product.setPrice(price);
        product.setMeasureUnit(measureUnit);

        JOptionPane.showMessageDialog(parent, "Product is updated!");
