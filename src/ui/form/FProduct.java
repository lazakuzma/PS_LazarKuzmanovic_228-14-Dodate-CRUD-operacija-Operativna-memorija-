    private void fillForm() {
        fillMeasurementUnits();
        fillManufacturers();
    }

    private void fillMeasurementUnits() {
        comboMeasureUnit.removeAllItems();

        comboMeasureUnit.addItem(MeasureUnit.KG);
        comboMeasureUnit.addItem(MeasureUnit.L);
        comboMeasureUnit.addItem(MeasureUnit.PCS);

    }

    private void fillManufacturers() {
        comboManufacturer.removeAllItems();

        List<Manufacturer> manufacturers = Controller.getInstance().getManufacturers();

        for (Manufacturer manufacturer : manufacturers) {
            comboManufacturer.addItem(manufacturer);
        }
    }

    private void prepareForm(ModeForm mode) {
        jtxtProductError.setEnabled(false);
        switch (mode) {
            case ADD:
                jbtnEnableChanges.setVisible(false);
                jbtnUpdate.setVisible(false);
                jbtnDelete.setVisible(false);
                break;
            case VIEW:
                jbtnEnableChanges.setVisible(true);
                jbtnUpdate.setVisible(false);
                btnSave.setVisible(false);
                jbtnDelete.setVisible(false);
                txtId.setText(product.getId().toString());
                txtName.setText(product.getName());
                txtPrice.setText(product.getPrice().toString());
                comboManufacturer.setSelectedItem(product.getManufacturer());
                comboMeasureUnit.setSelectedItem(product.getMeasureUnit());
                jtxtDescription.setText(product.getDescription());

                comboManufacturer.setEnabled(false);
                comboMeasureUnit.setEnabled(false);
                jbtnSelectManufacturer.setEnabled(false);
                txtId.setEditable(false);
                txtName.setEditable(false);
                txtPrice.setEditable(false);
                jtxtDescription.setEditable(false);
                break;
            case UPDATE:
                txtName.setEditable(true);
                txtPrice.setEditable(true);
                jtxtDescription.setEditable(true);
                comboManufacturer.setEnabled(true);
                comboMeasureUnit.setEnabled(true);
                jbtnSelectManufacturer.setEnabled(true);
                btnSave.setVisible(false);
                jbtnUpdate.setVisible(true);
                jbtnDelete.setVisible(true);

                break;

            default:;
        }
    }

